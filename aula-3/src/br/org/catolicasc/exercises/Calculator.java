public class Calculator {
    void sum(int x, int y) {
        System.out.println(x + y);
    }

    void sub(int x, int y) {
        System.out.println(x - y);
    }

    void mul(int x, int y) {
        System.out.println(x * y);
    }

    void div(int x, int y) {
        System.out.println(x / y);
    }
}
